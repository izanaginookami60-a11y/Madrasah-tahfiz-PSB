// Current language
var currentLang = localStorage.getItem('language') || 'ms';

// Get translation
function t(key) {
    var lang = translations[currentLang] || translations.ms;
    return lang[key] || translations.ms[key] || key;
}

// Change language
function changeLanguage(lang) {
    if (!translations[lang]) return;

    currentLang = lang;
    localStorage.setItem('language', lang);

    // Set RTL for Arabic
    if (lang === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.setAttribute('lang', 'ar');
        document.body.classList.add('rtl');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.setAttribute('lang', lang === 'en' ? 'en' : 'ms');
        document.body.classList.remove('rtl');
    }

    // Apply translations
    applyTranslations();

    // Show toast
    if (typeof showToast === 'function') {
        var msg = lang === 'ms' ? '✅ Bahasa ditukar ke Bahasa Melayu' :
                  lang === 'en' ? '✅ Language changed to English' :
                  '✅ تم تغيير اللغة إلى العربية';
        showToast(msg);
    }

    // Update language button
    updateLanguageButton();
}

function applyTranslations() {
    // All elements with data-i18n
    var elements = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < elements.length; i++) {
        var key = elements[i].getAttribute('data-i18n');
        var translation = t(key);
        if (translation && translation !== key) {
            elements[i].textContent = translation;
        }
    }

    // Placeholders
    var inputs = document.querySelectorAll('[data-i18n-placeholder]');
    for (var i = 0; i < inputs.length; i++) {
        var key = inputs[i].getAttribute('data-i18n-placeholder');
        var translation = t(key);
        if (translation && translation !== key) {
            inputs[i].setAttribute('placeholder', translation);
        }
    }

    // Titles
    var titles = document.querySelectorAll('[data-i18n-title]');
    for (var i = 0; i < titles.length; i++) {
        var key = titles[i].getAttribute('data-i18n-title');
        var translation = t(key);
        if (translation && translation !== key) {
            titles[i].setAttribute('title', translation);
        }
    }
}

function updateLanguageButton() {
    var btn = document.getElementById('langSwitcher');
    if (!btn) return;

    var flags = { ms: '🇲🇾', en: '🇬🇧', ar: '🇸🇦' };
    var names = { ms: 'BM', en: 'EN', ar: 'AR' };
    btn.innerHTML = flags[currentLang] + ' ' + names[currentLang];
}

// Init on page load
document.addEventListener('DOMContentLoaded', function() {
    setTimeout(function() {
        if (currentLang === 'ar') {
            document.documentElement.setAttribute('dir', 'rtl');
            document.body.classList.add('rtl');
        }
        applyTranslations();
        updateLanguageButton();
    }, 100);
});

// ===== AUTO TAMBAH data-i18n KEPADA ELEMENT =====
function autoAddI18nTags() {
    // Auto tag untuk tab buttons
    var tabButtons = document.querySelectorAll('.tab-btn');
    for (var i = 0; i < tabButtons.length; i++) {
        var btn = tabButtons[i];
        var onclick = btn.getAttribute('onclick') || '';

        if (onclick.indexOf("'students'") > -1) btn.setAttribute('data-i18n', 'tab.students');
        else if (onclick.indexOf("'addStudent'") > -1) btn.setAttribute('data-i18n', 'tab.addStudent');
        else if (onclick.indexOf("'addRecord'") > -1) btn.setAttribute('data-i18n', 'tab.addRecord');
        else if (onclick.indexOf("'attendance'") > -1) btn.setAttribute('data-i18n', 'tab.attendance');
        else if (onclick.indexOf("'cashbook'") > -1) btn.setAttribute('data-i18n', 'tab.cashbook');
        else if (onclick.indexOf("'profiles'") > -1) btn.setAttribute('data-i18n', 'tab.profiles');
        else if (onclick.indexOf("'reports'") > -1) btn.setAttribute('data-i18n', 'tab.reports');
        else if (onclick.indexOf("'gallery'") > -1) btn.setAttribute('data-i18n', 'tab.gallery');
        else if (onclick.indexOf("'backup'") > -1) btn.setAttribute('data-i18n', 'tab.backup');
    }

    // Auto tag untuk logout button
    var logoutBtns = document.querySelectorAll('.btn-logout');
    for (var i = 0; i < logoutBtns.length; i++) {
        logoutBtns[i].setAttribute('data-i18n', 'nav.logout');
    }

    // Auto tag untuk nav brand
    var navBrand = document.querySelector('.nav-brand span:last-child');
    if (navBrand) {
        if (navBrand.textContent.indexOf('Guru') > -1) {
            navBrand.setAttribute('data-i18n', 'nav.admin');
        } else if (navBrand.textContent.indexOf('Bapa') > -1) {
            navBrand.setAttribute('data-i18n', 'nav.parent');
        }
    }

    // Auto tag untuk stat labels
    var statLabels = document.querySelectorAll('.stat-info p');
    for (var i = 0; i < statLabels.length; i++) {
        var text = statLabels[i].textContent.trim();
        if (text.indexOf('Pelajar') > -1 && text.indexOf('Jumlah') > -1) {
            statLabels[i].setAttribute('data-i18n', 'stats.totalStudents');
        } else if (text.indexOf('Rekod') > -1) {
            statLabels[i].setAttribute('data-i18n', 'stats.totalRecords');
        } else if (text.indexOf('Purata') > -1) {
            statLabels[i].setAttribute('data-i18n', 'stats.avgScore');
        } else if (text.indexOf('Khatam') > -1) {
            statLabels[i].setAttribute('data-i18n', 'stats.completedJuz');
        } else if (text.indexOf('Kehadiran') > -1) {
            statLabels[i].setAttribute('data-i18n', 'stats.todayAttendance');
        }
    }
}

